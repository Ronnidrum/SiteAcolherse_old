            <h3>Arteterapia</h3>
            A Arteterapia nada mais é do que a terapia através da arte, a possibilidade de se expressar através da arte e o
            se conhecer e se entender através dessa arte. Não requer um conhecimento de técnicas artísticas, pois seu
            principal objetivo é voltado para a sua arte pessoal, o que você deseja expor através da arte. Esse é um espaço
            muito propício para aquelas pessoas que possuem dificuldade de se expressar verbalmente.
            Faixa etária atendida:
            Crianças (5 às 10 anos de idade)
            Adolescentes (11 anos à 18 anos)
            Adultos e idosos</p>

            <p>
            <h3>Psicanálise</h3>
            A psicanálise é uma abordagem analítica, desenvolvida por Sigmund Freud, que utiliza de ferramentas como a livre
            associação, interpretações e da relação construída entre analista e analisando, chamada de relação
            transferencial, para possibilitar a lembrança e elaboração de conteúdos inconscientes. É uma ferramenta que
            considera o sujeito em suas particularidades, auxiliando-o na busca de autoconhecimento, aceitação de suas
            limitações e possibilidades, identificando suas defesas, emoções e desejos.

            Atendimento a adolescentes a partir de 16 anos, adultos e idosos.</p>

            <p>
            <h3>Psicologia Junguiana</h3>

            A psicologia Junguiana, criada por Carl Gustav Jung, é uma abordagem analítica que pode ser experienciada por
            qualquer pessoa independente da idade, mas que aborda as temáticas da vida de forma mais simbólica, levando em
            consideração a sua opinião e o seu modo de ver as coisas, independe do significado popular das mesmas. Aborda,
            caso o paciente sinta-se confortável, a interpretação de sonhos em conjunto com as demandas trazidas nas
            sessões. Utiliza as mais diversas ferramentas através da Arteterapia.
            Faixa etária atendida:
            Crianças (5 às 10 anos de idade)
            Adolescentes (11 anos à 18 anos)
            Adultos e idosos</p>
