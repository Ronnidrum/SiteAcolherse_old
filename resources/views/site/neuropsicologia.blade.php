                    perceptual);</li>
                <li>Avaliação da atenção, funcionamento executivo e memória;</li>
                <li>Avaliação de aspectos afetivos e emocionais;</li>
            </ul>
            Análise do auto relato através de questionário descritivo de comportamentos.</p>

