                <center>
                    <h2>Quando procurar uma Avaliação Neuropsicológica?</h2>
                </center>
                Crianças e Adolescentes:
            <ul>
                <li>Problemas de aprendizagem;
                <li>Dificuldade para concentrar-se;
                <li>Dificuldade no processo de socialização;
                <li>Hiperatividade;
                <li>Comportamento opositor;
                <li>Questões neurológicas (avaliação pré e pós-cirúrgica, epilepsia, lesão encefálica).
            </ul>

            <ul>Adultos e Idosos:
                <li>Dificuldades para concentrar-se;
                <li>Queixas com memória;
                <li>Depressão;
                <li>Alteração repentina de comportamento;
                <li>Dificuldade com as interações sociais;
                <li>Questões neurológicas (avaliação pré e pós-cirúrgica, epilepsia, lesão encefálica).
            </ul>
            </p>

            <p>
