                    compensatórias para reduzir os efeitos de déficits cognitivos ou alterações de comportamento.</li>
            </ul>
            </p>

            <p>
                <center>
                    <h2>O que é a Estimulação Cognitiva?</h2>
                </center>
                Trata-se de um processo terapêutico planejado de acordo com as necessidades individuais, com o
                desenvolvimento de tarefas para estimular domínios cognitivos de modo a preservá-los, evitando ou diminuindo
                a piora cognitiva, muito utilizada em casos de envelhecimento e demências.
